import UIKit

extension UIColor {
	class func fidoYellowColor() -> UIColor {
		return UIColor(red:255/255.0, green:230/255.0, blue:0/255.0, alpha:255/255.0)
	}
	
	class func fidoTealColor() -> UIColor {
		return UIColor(red:0/255.0, green:176/255.0, blue:171/255.0, alpha:255/255.0)
	}
	
	class func lightGreyBackgroundColor() -> UIColor {
		return UIColor(red:243/255.0, green:243/255.0, blue:243/255.0, alpha:255/255.0)
	}
	
	class func mediumGreyBackgroundColor() -> UIColor {
		return UIColor(red:210/255.0, green:212/255.0, blue:213/255.0, alpha:255/255.0)
	}
	
	class func fidoDarkGreyColor() -> UIColor {
		return UIColor(red:91/255.0, green:103/255.0, blue:112/255.0, alpha:255/255.0)
	}
}
